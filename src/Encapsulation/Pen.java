package Encapsulation;

public class Pen {

    private String color;
    private int tip;

    public String getColor(){
        return this.color;
    }
    public void setColor(String color){
        this.color=color;
    }

    public int getTip(){
        return this.tip;
    }
    public void setTip(int tip){
        this.tip=tip;
    }
}
